package aula01_java.mesa_de_trabalho_mascote;

public class Mascote {
    //vou especificar o que vou fazer primeiro/ vai ter um tipo do tipo string
    String tipo;
    String nome;
    int idade;
    float qtdComida;
    String som;

    public Mascote(String tipo, String nome, int idade, float qtdComida, String som) {
        this.tipo = tipo;
        this.nome = nome;
        this.idade = idade;
        this.qtdComida = qtdComida;
        this.som = som;
    }
}
